package org.gentrifiedApps.gentrifiedAppsUtil.classExtenders.gamepad

/**
 * Enum class for all boolean buttons on a gamepad
 */
enum class Button {
    CROSS,
    CIRCLE,
    TRIANGLE,
    SQUARE,
    L1,
    R1,
    SHARE,
    OPTIONS,
    TOUCHPAD,
    LEFT_STICK,
    RIGHT_STICK,
    DPAD_UP,
    DPAD_DOWN,
    DPAD_LEFT,
    DPAD_RIGHT,
    RIGHT_BUMPER,
    LEFT_BUMPER,
}

/**
 * Enum class for all float buttons on a gamepad
 */
enum class FloatButton {
    LEFT_X,
    LEFT_Y,
    RIGHT_X,
    RIGHT_Y,
    LEFT_TRIGGER,
    RIGHT_TRIGGER,
    TOUCH_X,
    TOUCH_Y,
}